void NOKIA_Select(void) {
    HAL_GPIO_WritePin(NOKIA_SCE_PORT, NOKIA_SCE_PIN, GPIO_PIN_RESET);
}

void NOKIA_Unselect(void) {
    HAL_GPIO_WritePin(NOKIA_SCE_PORT, NOKIA_SCE_PIN, GPIO_PIN_SET);
}

void NOKIA_SendCmd(uint8_t *cmd, uint16_t size)
{
	if (NOKIA_StartDataPrepare()) {
	memcpy(s_framebuf[current_draw_frame], cmd, size);
	s_datatype[current_draw_frame] = NOKIA_CMD_TYPE;
	s_datasize[current_draw_frame] = size;
	NOKIA_StopDataPrepare();
	}

	if (!spi_busy) {
		spi_busy = true;
		if (NOKIA_SwapDisplayFrame()) {
			HAL_GPIO_WritePin(NOKIA_DC_PORT, NOKIA_DC_PIN, GPIO_PIN_RESET); // command
			NOKIA_Select();
			HAL_SPI_Transmit_DMA(s_hspi, s_framebuf[current_display_frame], s_datasize[current_display_frame]);
		}
	}
}

void NOKIA_SendData(void) {
	if (!spi_busy) {
		spi_busy = true;
		if (NOKIA_SwapDisplayFrame()) {
			HAL_GPIO_WritePin(NOKIA_DC_PORT, NOKIA_DC_PIN, GPIO_PIN_SET); // data
			NOKIA_Select();
			HAL_SPI_Transmit_DMA(s_hspi, s_framebuf[current_display_frame], s_datasize[current_display_frame]);
		}
	}
}

void NOKIA_SetPixel(uint8_t x, uint8_t y, bool bw) {
    if (x >= 0 && x < NOKIA_PANEL_WIDTH && y >= 0 && y < NOKIA_PANEL_HEIGHT) {
		int index = x + (y / 8) * NOKIA_PANEL_WIDTH;

		if (bw)
			s_framebuf[current_draw_frame][index] |= (1 << (y % 8));
		else
			s_framebuf[current_draw_frame][index] &= ~(1 << (y % 8));
    }
}

void NOKIA_SetLine(int x0, int y0, int x1, int y1, bool bw) {
	int dy = y1 - y0; // Difference between y0 and y1
	int dx = x1 - x0; // Difference between x0 and x1
	int stepx, stepy;

	if (dy < 0)
	{
		dy = -dy;
		stepy = -1;
	}
	else
		stepy = 1;

	if (dx < 0)
	{
		dx = -dx;
		stepx = -1;
	}
	else
		stepx = 1;

	dy <<= 1; // dy is now 2*dy
	dx <<= 1; // dx is now 2*dx
	NOKIA_SetPixel(x0, y0, bw); // Draw the first pixel.

	if (dx > dy)
	{
		int fraction = dy - (dx >> 1);
		while (x0 != x1)
		{
			if (fraction >= 0)
			{
				y0 += stepy;
				fraction -= dx;
			}
			x0 += stepx;
			fraction += dy;
			NOKIA_SetPixel(x0, y0, bw);
		}
	}
	else
	{
		int fraction = dx - (dy >> 1);
		while (y0 != y1)
		{
			if (fraction >= 0)
			{
				x0 += stepx;
				fraction -= dy;
			}
			y0 += stepy;
			fraction += dx;
			NOKIA_SetPixel(x0, y0, bw);
		}
	}
}

void NOKIA_SetRect(int x0, int y0, int x1, int y1, bool fill, bool bw) {
	// check if the rectangle is to be filled
	if (fill == 1)
	{
		int xDiff;

		if(x0 > x1)
			xDiff = x0 - x1; //Find the difference between the x vars
		else
			xDiff = x1 - x0;

	while(xDiff > 0)
	{
		NOKIA_SetLine(x0, y0, x0, y1, bw);

		if(x0 > x1)
			x0--;
		else
			x0++;

		xDiff--;
		}
	}
	else
	{
		// best way to draw an unfilled rectangle is to draw four lines
		NOKIA_SetLine(x0, y0, x1, y0, bw);
		NOKIA_SetLine(x0, y1, x1, y1, bw);
		NOKIA_SetLine(x0, y0, x0, y1, bw);
		NOKIA_SetLine(x1, y0, x1, y1, bw);
	}
}

void NOKIA_SetCircle(int x0, int y0, int radius, bool bw, int lineThickness) {
	for(int r = 0; r < lineThickness; r++)
	{
		int f = 1 - radius;
		int ddF_x = 0;
		int ddF_y = -2 * radius;
		int x = 0;
		int y = radius;

		NOKIA_SetPixel(x0, y0 + radius, bw);
		NOKIA_SetPixel(x0, y0 - radius, bw);
		NOKIA_SetPixel(x0 + radius, y0, bw);
		NOKIA_SetPixel(x0 - radius, y0, bw);

		while(x < y)
		{
			if(f >= 0)
			{
				y--;
				ddF_y += 2;
				f += ddF_y;
			}
			x++;
			ddF_x += 2;
			f += ddF_x + 1;

			NOKIA_SetPixel(x0 + x, y0 + y, bw);
			NOKIA_SetPixel(x0 - x, y0 + y, bw);
			NOKIA_SetPixel(x0 + x, y0 - y, bw);
			NOKIA_SetPixel(x0 - x, y0 - y, bw);
			NOKIA_SetPixel(x0 + y, y0 + x, bw);
			NOKIA_SetPixel(x0 - y, y0 + x, bw);
			NOKIA_SetPixel(x0 + y, y0 - x, bw);
			NOKIA_SetPixel(x0 - y, y0 - x, bw);
		}
		radius--;
	}
}

void NOKIA_SetChar(char character, int x, int y, bool bw, bool transparent)
{
	int column; // temp byte to store character's column bitmap
	for (int i=0; i<5; i++) // 5 columns (x) per character
	{
		column = ASCII[character - 0x20][i];
		for (int j=0; j<8; j++) // 8 rows (y) per character
		{
			if (column & (0x01 << j)) {// test bits to set pixels
				if ((transparent && bw != 0) || !transparent) {
					NOKIA_SetPixel(x+i, y+j, bw);
				}
			}
			else {
				if ((transparent && !bw != 0) || !transparent) {
					NOKIA_SetPixel(x+i, y+j, !bw);
				}
			}
		}
	}
}

void NOKIA_SetStr(char * dString, int x, int y, bool bw, bool transparent)
{
	while (*dString != 0x00) // loop until null terminator
	{
		NOKIA_SetChar(*dString++, x, y, bw, transparent);
		x+=5;
		if ((transparent && !bw != 0) || !transparent) {
			for (int i=y; i<y+8; i++)
			{
				NOKIA_SetPixel(x, i, !bw);
			}
		}
		x++;
		if (x > (NOKIA_PANEL_WIDTH - 5)) // Enables wrap around
		{
			x = 0;
			y += 8;
		}
	}
}


bool NOKIA_StartDataPrepare(void) {
	if (current_display_frame == (current_draw_frame ^ 1)) {
		isDrawing = false;
		return false;
	}
	current_draw_frame ^= 1;
	isDrawing = true;
	s_datatype[current_draw_frame] = NOKIA_DATA_TYPE;
	s_datasize[current_draw_frame] = NOKIA_PANEL_DATA_SIZE;
	return true;
}

void NOKIA_StopDataPrepare(void) {
	isDrawing = false;
	alreadyDisplayed = false;
}

bool NOKIA_SwapDisplayFrame(void) {
	if (!alreadyDisplayed && (!isDrawing || current_draw_frame != (current_display_frame ^ 1))) {
		current_display_frame ^= 1;
		alreadyDisplayed = true;
		return true;
	}
	return false;
}

void NOKIA_Reset(void)
{
    HAL_GPIO_WritePin(NOKIA_RST_PORT, NOKIA_RST_PIN, GPIO_PIN_RESET);
    HAL_Delay(10);
    HAL_GPIO_WritePin(NOKIA_RST_PORT, NOKIA_RST_PIN, GPIO_PIN_SET);
}

void NOKIA_Init(SPI_HandleTypeDef *hspi)
{
	s_hspi = hspi;
	
    NOKIA_Reset();
    NOKIA_Clear();

    uint8_t cmd[] = {
    		0x21,	// Extended instruction set
			0xBF,	// Contrast
			0x04,	// Temp coefficient
			0x14,	// Bias mode
			0x20,	// Basic instruction set
			0x0C	// Normal display (not inverted)
    };

    NOKIA_SendCmd(cmd, 6);
}

void HAL_SPI_TxCpltCallback(SPI_HandleTypeDef *hspi)
{
    if (hspi->Instance == SPI1) {
    	if (NOKIA_SwapDisplayFrame() && hspi->State == HAL_SPI_STATE_READY) {
    		HAL_GPIO_WritePin(NOKIA_DC_PORT, NOKIA_DC_PIN, s_datatype[current_display_frame] ? GPIO_PIN_SET : GPIO_PIN_RESET);
    		NOKIA_Select();

    		HAL_SPI_Transmit_DMA(s_hspi, s_framebuf[current_display_frame], s_datasize[current_display_frame]);
    	} else {
			NOKIA_Unselect();
			spi_busy = false;
    	}
    }
}

void NOKIA_Clear(void)
{
	memset(s_framebuf, 0, sizeof(s_framebuf));
}
